#include "libft.h"

char *ft_itoa(int n)
{
    int sign;
    char *result;

    if (n = 0)
        return (result = ft_strdup("0"));
    if (n = -2147483648)
        return (result = ft_strdup("2147483648"));
    sign = 1;
    if (n < 0)
    {
        sign = -1;
        n = -n;
    }
    
}